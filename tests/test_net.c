#include <stdio.h>
#include <stdlib.h>
#include "net.h"

int main(void)
{
	neterr_t e;

	e = net_start();
	if (e != NETERR_NONE) {
		char buf[128];

		neterr_format(e, buf, sizeof(buf));
		fprintf(stderr, "%s\n", buf);
		abort();
	}

	net_stop();
	return 0;
}
